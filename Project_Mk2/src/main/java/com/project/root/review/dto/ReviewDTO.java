package com.project.root.review.dto;

public class ReviewDTO {
	private int reNo;
	private int basNo;
	private int reSco;
	private String reContent;
	
	public int getReNo() {
		return reNo;
	}
	public void setReNo(int reNo) {
		this.reNo = reNo;
	}
	public int getBasNo() {
		return basNo;
	}
	public void setBasNo(int basNo) {
		this.basNo = basNo;
	}
	public int getReSco() {
		return reSco;
	}
	public void setReSco(int reSco) {
		this.reSco = reSco;
	}
	public String getReContent() {
		return reContent;
	}
	public void setReContent(String reContent) {
		this.reContent = reContent;
	}
	
	
}
